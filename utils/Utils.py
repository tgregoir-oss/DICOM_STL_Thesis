def map_points_to_frame(a, GO, size):
    new_x = (a.x - GO.center_x) / (GO.max_x - GO.min_x)
    new_y = (a.y - GO.center_y) / (GO.max_y - GO.min_y)
    return [int(new_x * size), int(new_y * size)]


def create_bitmap(one_model_data, one_model_global_offset, definition):
    one_contour_z_values = []
    model_bitmap = []
    for i in range(len(one_model_data)):
        sub = []
        one_contour_z_values.append(one_model_data[i][0][0].z)
        for j in one_model_data[i]:
            vertices = []
            for p in j:
                vertices.append(map_points_to_frame(p, one_model_global_offset, definition))

            vertices = np.asarray(vertices)

            image = np.full((definition, definition), fill_value=0, dtype=np.uint8)

            path = mpath.Path(vertices)

            # Get the coordinates of all points inside the bounding box
            x, y = np.meshgrid(np.arange(-definition / 2, definition / 2), np.arange(-definition / 2, definition / 2))
            points = np.column_stack((x.ravel(), y.ravel()))

            # Use Path.contains_points to get a boolean array indicating which points are inside the path
            mask = path.contains_points(points).reshape(image.shape)

            # Use the mask to fill the region inside the polygon with the desired value
            image[mask] = 1
            sub.append(image)
        model_bitmap.append(sub)
    return model_bitmap, one_contour_z_values


def Smooth_Mesh(STL_Path, type=0, fichier=0):
    reader = vtk.vtkSTLReader()
    reader.SetFileName(STL_Path)
    reader.Update()
    mesh_data = reader.GetOutput()
    if type == 0:
        gaussian_filter = vtk.vtkSmoothPolyDataFilter()
        gaussian_filter.SetInputData(mesh_data)
        gaussian_filter.SetNumberOfIterations(70)
        gaussian_filter.SetRelaxationFactor(0.1)

        gaussian_filter.SetBoundarySmoothing(False)

        gaussian_filter.Update()
        filtered_mesh_data = gaussian_filter.GetOutput()
    else:
        taubin_filter = vtk.vtkWindowedSincPolyDataFilter()
        taubin_filter.SetInputData(mesh_data)
        taubin_filter.SetNumberOfIterations(100)
        taubin_filter.SetPassBand(0.05)
        taubin_filter.NormalizeCoordinatesOn()
        taubin_filter.Update()
        filtered_mesh_data = taubin_filter.GetOutput()
    writer = vtk.vtkSTLWriter()
    if fichier == 0:
        writer.SetFileTypeToBinary()
    else:
        writer.SetFileTypeToASCII()
    writer.SetFileName(STL_Path)
    writer.SetInputData(filtered_mesh_data)
    writer.Write()
